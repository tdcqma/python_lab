# %s -> 字符串
print("My name is %s" %("Alfred.Xue"))

# %d -> 数字
print("I am %d years old." %(25))

# %f -> 浮点数
print("his height is %f m" % (1.701))

# %.2f -> 浮点数,指定保留两位小数
print("his height is %.2f" % (1.701))

