# 变量的两个作用： 存储、调用

#整数变量
a = 1
b = 2
print(a)
print(a+b)

#字符串变量
name = 'harry'
print(name)

#布尔值变量
Answer_1 = False
Answer_2 = True
print(Answer_1,Answer_2)


#赋值
#等号=是赋值语句，可以把任意数据类型赋值给变量，同一个变量可以反复赋值，而且可以是不同类型的变量，例如：

test1 = 1
print("test1=", test1)
test1 = 'abc'
print("test1=", test1)

#除法，一种除法是/ 计算结果是浮点数，即使是两个整数恰好整除，结果也是浮点数，
#     另一种除法是//，称为地板除，两个整数的除法仍然是整数：
print("10/3=",10/3)
print("10//3=",10//3)
